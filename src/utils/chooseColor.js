export default function chooseColor(projectTitle) {
  switch (projectTitle) {
    case "3a9b8513-5b86-44a9-9500-f91f9ce70890":
      return "#6DED4A";
    case "7f9013ad-5846-4492-99ee-5c66cf4bf711":
      return "#8e8ee2";
    case "d437a404-dd6a-4113-8ae9-c8b8864b6a89":
      return "#d7192c";
    default:
      return "#ff0";
  }
}
